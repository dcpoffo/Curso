    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        Turma turma = new Turma();
        
        String nome = jTextFieldTurma.getText();
        if (nome.trim().length() < 2){
            JOptionPane.showMessageDialog(this, "Nome deve conter entre 2 e 80 caracteres", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        turma.setTurma(nome);
        
        if (this.id == STATUS_CADASTRO) {
            int id = dao.inserir(turma);
            JOptionPane.showMessageDialog(this, "Turma cadastrada com sucesso!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            jTextFieldTurma.setText("");
            jTextFieldTurma.requestFocus();
        } else {
            turma.setId(this.id);
            boolean alterou = dao.alterar(turma);
            if (alterou) {
                JOptionPane.showMessageDialog(this, "Nome da turma alterada com sucesso!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                dispose(); 
            } else {
                JOptionPane.showMessageDialog(this, "NÃ£o foi possivel alterar o nome da turma!", "Aviso", JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_jButtonSalvarActionPerformed

